package ses.model.oms;

public class OrgInfo {
    
    private String id;
    
    private String purchaseUnitName;
    
    private String purchaseUnitDuty;
    
    private String orgId;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPurchaseUnitName() {
        return purchaseUnitName;
    }

    public void setPurchaseUnitName(String purchaseUnitName) {
        this.purchaseUnitName = purchaseUnitName;
    }

    public String getPurchaseUnitDuty() {
        return purchaseUnitDuty;
    }

    public void setPurchaseUnitDuty(String purchaseUnitDuty) {
        this.purchaseUnitDuty = purchaseUnitDuty;
    }

    public String getOrgId() {
        return orgId;
    }

    public void setOrgId(String orgId) {
        this.orgId = orgId;
    }
    
    

}
