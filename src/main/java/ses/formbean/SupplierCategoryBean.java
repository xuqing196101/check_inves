package ses.formbean;

public class SupplierCategoryBean {

	private String categoryName;
	
	private String lastOneYear;
	
	private String lastTwoYear;
	
	private String lastLastThreeYear;

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}

	public String getLastOneYear() {
		return lastOneYear;
	}

	public void setLastOneYear(String lastOneYear) {
		this.lastOneYear = lastOneYear;
	}

	public String getLastTwoYear() {
		return lastTwoYear;
	}

	public void setLastTwoYear(String lastTwoYear) {
		this.lastTwoYear = lastTwoYear;
	}

	public String getLastLastThreeYear() {
		return lastLastThreeYear;
	}

	public void setLastLastThreeYear(String lastLastThreeYear) {
		this.lastLastThreeYear = lastLastThreeYear;
	}
	
	
	
}
