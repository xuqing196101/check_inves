package yggc.model.bms;

public class RolePreMenu {
	
	/**
	 * @Fields role : 角色
	 */
	private Role role;
	
	/**
	 * @Fields preMenu :权限 
	 */
	private PreMenu preMenu;

	public Role getRole() {
		return role;
	}

	public void setRole(Role role) {
		this.role = role;
	}

	public PreMenu getPreMenu() {
		return preMenu;
	}

	public void setPreMenu(PreMenu preMenu) {
		this.preMenu = preMenu;
	}
	
}
